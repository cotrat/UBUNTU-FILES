#include <string>
#include <iostream>
#include "Player.h"
#include "FistfullODollars.h"

using namespace std;

FistfullODollars::FistfullODollars() : Player("FistfullODollars")  {
	ctr = 0;
}

FistfullODollars::~FistfullODollars() {}

void FistfullODollars::setCtr(int newCtr)
{
	ctr = newCtr;
}


string FistfullODollars::performMove()
{
	if (ctr == 0)
	{
		return "Rock";
	}
	if (ctr == 1)
	{
		return "Paper";
	}
	if (ctr == 2)
	{
		return "Paper";
	}
}

